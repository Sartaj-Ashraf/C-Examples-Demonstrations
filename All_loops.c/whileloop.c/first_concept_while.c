#include <stdio.h>

int main()
{
    int a;
    printf(" the numbers less than 1000 are  \n");
    while (a <= 1000)   /*condition terms*/
    {
       printf(" %d\n", a);
        a++;  /*a++ = a=a+1*/
    }
}

